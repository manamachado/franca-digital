// OPERADORES

// Boa hora pra anotar!

// No JS, usamos os mesmos operadores aritméticos de uma calculadora, porém, temos um operador que funciona um pouco diferente, que é o resto da divisão %

// Adição
1 + 1

// Subtração
4 - 2

// Multiplicação
2 * 4

// Divisão
4 / 2

// Resto da divisão
10 % 2

// Exponenciação
2 ** 4

// Temos também os operadores de comparação, geralmente usamos esses operadores quando temos que tomar alguma decisão

// Menor que
1 < 2

// Maior que
4 > 2

// Igual a
1 == "1"

// Igual a (compara o tipo, boa prática)
1 === 1

// Diferente de
2 != 1

// Diferente de (compara o tipo, boa prática)
1 !== "1"

// Menor ou igual que
10 <= 10

// Maior ou igual que
5 >= 4

// Ou
(idade < 18) || (idade > 25)
// Aqui no operador OU, ele deve satisfazer uma das condições

// E
(nome === "Maná") && (idade < 25)
// Aqui no operador E, ele deve satisfazer as duas condições

// Vamos codar um pouco
console.log('1 + 1 é: ', 1 + 1)
console.log('4 - 2 é: ', 4 - 2)
console.log('2 * 3 é: ', 2 * 3)
console.log('6 / 3 é: ', 6 / 3)
console.log('4 % 2 é: ', 4 % 2)
console.log('2 ** 4 é: ', 2 ** 4)

